package com.cpseeker.treegen;

public class UnsupportedInputStreamException extends RuntimeException
{	
	/**
	 * 
	 */
	private static final long serialVersionUID = 2846235484167233858L;
	public UnsupportedInputStreamException() 
	{ 
		super(); 
	}
	public UnsupportedInputStreamException(String s) 
	{ 
		super(s); 
	}
	public UnsupportedInputStreamException(String s, Throwable throwable) 
	{ 
		super(s, throwable); 
	}
	public UnsupportedInputStreamException(Throwable throwable) 
	{ 
		super(throwable); 
	}
	
}
